package Modelo;

public class Cargo {
    private int codigo;
    private int tipou;
    private int estado;

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public int getTipou() {
        return tipou;
    }

    public void setTipou(int tipou) {
        this.tipou = tipou;
    }

    public int getEstado() {
        return estado;
    }

    public void setEstado(int estado) {
        this.estado = estado;
    }
    
    
}
